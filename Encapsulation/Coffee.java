package Encapsulation;

public class Coffee {

    private int price;

    private String flavour;

    public int getPrice() {
        return price;
    }


    public void setPrice(int price) {
        this.price = price;
    }
    public String getFlavour() {
        return flavour;
    }

    public void setFlavour(String flavour) {
        this.flavour = flavour;
    }
}
